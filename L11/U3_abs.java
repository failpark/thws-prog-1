public class U3_abs {
	public static void main() {
	}

	public static int abs(int val) {
		if (val < 0) {
			val *= -1;
		}
		return val;
	}

	public static float abs(float val) {
		if (val < 0) {
			val *= -1;
		}
		return val;
	}

	public static double abs(double val) {
		if (val < 0) {
			val *= -1;
		}
		return val;
	}

	public static long abs(long val) {
		if (val < 0) {
			val *= -1;
		}
		return val;
	}

	public static short abs(short val) {
		if (val < 0) {
			val *= -1;
		}
		return val;
	}
}
